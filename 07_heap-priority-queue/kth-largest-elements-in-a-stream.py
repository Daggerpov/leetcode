"""
In constructor: Heapify nums, then while it's too big, pop (will pop min element automatically in Python). Then, in add: add it, then if new length exceeds k, pop. Return smallest number (which will be the kth largest, by logic).
"""

class KthLargest:

    def __init__(self, k: int, nums: List[int]):
        self.minHeap = nums
        self.k = k
        heapq.heapify(self.minHeap)
        while len(self.minHeap) > k:
            heapq.heappop(self.minHeap)

    def add(self, val: int) -> int:
        heapq.heappush(self.minHeap, val)
        if len(self.minHeap) > self.k:
            heapq.heappop(self.minHeap)
        return self.minHeap[0]
